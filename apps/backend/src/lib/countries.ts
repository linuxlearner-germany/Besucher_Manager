export type CountryCode = (typeof ISO_COUNTRY_CODES)[number];
export type Country = { code: CountryCode; name: string };

const codeSet = new Set<string>(ISO_COUNTRY_CODES);
const displayNames = new Intl.DisplayNames(["de"], { type: "region" });

export const COUNTRIES: Country[] = ISO_COUNTRY_CODES
  .map((code) => ({ code, name: displayNames.of(code) || code }))
  .sort((left, right) => left.name.localeCompare(right.name, "de"));

export function normalizeCountryCode(value: unknown): CountryCode | null {
  if (typeof value !== "string") return null;
  const normalized = value.trim().toUpperCase();
  return codeSet.has(normalized) ? normalized as CountryCode : null;
}

export function getCountryName(value: string | null | undefined): string | null {
  const code = normalizeCountryCode(value);
  return code ? displayNames.of(code) || code : null;
}

export function findCountryCode(value: unknown): CountryCode | null {
  const direct = normalizeCountryCode(value);
  if (direct) return direct;
  if (typeof value !== "string") return null;
  const normalizedName = value.trim().toLocaleLowerCase("de");
  return COUNTRIES.find((country) => country.name.toLocaleLowerCase("de") === normalizedName)?.code ?? null;
}
